def sumar(op1, op2):
    print(f"El resultado de {op1} mas {op2} es: {op1+op2}")
    
def restar(op1, op2):
    print(f"El resultado de {op1} menos {op2} es: {op1-op2}")
    
def multiplicar(op1, op2):
    print(f"El resultado de {op1} por {op2} es: {op1*op2}")

def dividir(dividendo, divisor):
    print(f"El resultado de {dividendo} entre {divisor} es {dividendo/divisor}")